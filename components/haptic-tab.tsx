import { BottomTabBarButtonProps } from '@react-navigation/bottom-tabs';
import { PlatformPressable } from '@react-navigation/elements';
import * as Haptics from 'expo-haptics';
// The HapticTab component is a custom tab bar button that provides haptic feedback when pressed. It uses the PlatformPressable component from React Navigation to handle press events, and triggers a light haptic impact on iOS devices when the tab is pressed. This enhances the user experience by providing tactile feedback when navigating between tabs in the app.
export function HapticTab(props: BottomTabBarButtonProps) {
  return (
    <PlatformPressable
      {...props}
      onPressIn={(ev) => {
        if (process.env.EXPO_OS === 'ios') {
          // Add a soft haptic feedback when pressing down on the tabs.
          Haptics.impactAsync(Haptics.ImpactFeedbackStyle.Light);
        }
        props.onPressIn?.(ev);
      }}
    />
  );
}
